// ── Public signals tests ──

function test_public_signals() {
  const signals = ["33", "1", "5", "123", "456", "789"];
  const buf = convertPublicSignals(signals);
  // 4 bytes len + 6 * 32 bytes
  const expectedSize = 4 + 6 * 32;
  assert.equal(buf.length, expectedSize, `Public signals size: ${buf.length} != ${expectedSize}`);
  const len = buf.readUInt32BE(0);
  assert.equal(len, 6, `Signal count: ${len} != 6`);
  ok('Public signals conversion correct');
}

