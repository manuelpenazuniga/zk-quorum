function test_g1_identity() {
  const vk = {
    protocol: "groth16",
    curve: "bls12381",
    vk_alpha_1: ["0","0","0"],
    vk_beta_2: [["0","0"],["0","0"],["0","0"]],
    vk_gamma_2: [["0","0"],["0","0"],["0","0"]],
    vk_delta_2: [["0","0"],["0","0"],["0","0"]],
    IC: [["1","2","1"]]
  };
  const buf = convertVk(vk);
  // First byte of alpha should be the identity flag
  assert.equal(buf[0], 0x40, 'G1 identity flag');
  // First byte of beta should also be identity (G2 infinity)
  assert.equal(buf[96], 0x40, 'G2 identity flag');
  ok('Identity points encoded correctly');
}

